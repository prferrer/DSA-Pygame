def init_map(map_name):
    global _original_grid, map_grid, MAP_ROWS, MAP_COLS, TILE_SIZE, offset_x, offset_y, shrink_layer, map_data, _current_map_name

    _current_map_name = map_name 
    _original_grid = _GRIDS.get(map_name, _GRIDS["The Underkeep"])
    map_grid = copy.deepcopy(_original_grid)
    MAP_ROWS = len(map_grid)
    MAP_COLS = len(map_grid[0])
    TILE_SIZE = min(WIDTH // MAP_COLS, HEIGHT // MAP_ROWS)
    offset_x = (WIDTH - (MAP_COLS * TILE_SIZE)) // 2
    offset_y = (HEIGHT - (MAP_ROWS * TILE_SIZE)) // 2
    shrink_layer = 0

    print(f"[init_map] map={map_name}, p1 spawn={_SPAWNS.get(map_name, {}).get('p1')}, p2 spawn={_SPAWNS.get(map_name, {}).get('p2')}")
    map_data = MapData()

def reset_grid():
    global map_grid, shrink_layer
    map_grid = copy.deepcopy(_original_grid)
    shrink_layer = 0

def shrink_map(players):
    global shrink_layer, map_grid
    shrink_layer += 1
    sl = shrink_layer
    for y in range(MAP_ROWS):
        for x in range(MAP_COLS):
            # Turn edges into walls
            if x < sl or x >= MAP_COLS - sl or y < sl or y >= MAP_ROWS - sl:
                map_grid[y][x] = 1

    # Bump players out of walls safely
    for p in players:
        if map_grid[p.pos[1]][p.pos[0]] == 1:
            p.pos = get_nearest_safe_tile(p.pos)

def get_nearest_safe_tile(pos):
    cx, cy = pos
    for r in range(1, max(MAP_COLS, MAP_ROWS)):
        for dy in range(-r, r + 1):
            for dx in range(-r, r + 1):
                nx, ny = cx + dx, cy + dy
                if 0 <= nx < MAP_COLS and 0 <= ny < MAP_ROWS:
                    if map_grid[ny][nx] == 0:
                        return [nx, ny]
                    
    print(f"[get_valid_spawn] map={_current_map_name}, is_p1={is_p1}, desired={desired}, tile={map_grid[y][x]}")
    return pos

def get_valid_spawn(is_p1):
    spawns = _SPAWNS.get(_current_map_name, _SPAWNS["The Underkeep"])
    desired = spawns["p1"] if is_p1 else spawns["p2"]

    # Use the designated tile if it's open, otherwise search nearby
    x, y = desired
    if 0 <= x < MAP_COLS and 0 <= y < MAP_ROWS and map_grid[y][x] == 0:
        return [x, y]

    # Fallback: find nearest open tile to the desired spawn
    return get_nearest_safe_tile(desired)

class MapData:
    def __init__(self):
        self.TILE_SIZE = TILE_SIZE
        self.offset_x = offset_x
        self.offset_y = offset_y
        self.MAP_COLS = MAP_COLS
        self.MAP_ROWS = MAP_ROWS

    @property
    def map_grid(self):
        return map_grid

    def draw_map(self, screen):
        for row in range(MAP_ROWS):
            for col in range(MAP_COLS):
                if map_grid[row][col] == 1:
                    if (col < shrink_layer or col >= MAP_COLS - shrink_layer or row < shrink_layer or row >= MAP_ROWS - shrink_layer):
                        pygame.draw.rect(screen, (80, 80, 80), (
                            self.offset_x + col * self.TILE_SIZE,
                            self.offset_y + row * self.TILE_SIZE,
                            self.TILE_SIZE, self.TILE_SIZE
                        ))
